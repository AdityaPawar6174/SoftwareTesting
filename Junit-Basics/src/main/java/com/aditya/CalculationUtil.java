package com.aditya;

public class CalculationUtil {

    public int add(int a, int b){
        return a + b;
    }

    public int multiply(int a, int b){
        return a * b;
    }

    public String parseString(String n)
    {
        return n;
    }

    public int div(int a, int b)
    {
        return a/b;
    }

    public boolean isEven(int a)
    {
        return a % 2 == 0;
    }

    public boolean isOdd(int a)
    {
        return a % 2 != 0;
    }
}
